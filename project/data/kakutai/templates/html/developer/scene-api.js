// Scene API
// ----------------------------------------------------------------------------
//  For access to objects and scene elements this API should be used.
//  Using this API provides:
//       - Easy access to scene objects
//       - Easy control of scene objects and mapping to them and their parts (sensors and events included)
//       - Generic access to either local client simulation or backend simulation (you can choose)
//       - Ability to save and load your work. Non API code can only be run locally in the developer editor.
//       - Documentation for API functions and objects (none are provided for the internal system)
//       - Examples and tutorials are all using API calls. 
//       - Supported throughout lifetime of SEER - internal calls will change. Important - they WILL change.
